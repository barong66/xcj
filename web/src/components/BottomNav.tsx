export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-bg/95 backdrop-blur-md border-t border-border pb-[env(safe-area-inset-bottom)]">
      <div className="max-w-[430px] mx-auto flex items-center justify-around h-12">
        {tabs.map((tab) => {
          const isActive =
            tab.href === "/"
              ? pathname === "/" && !pathname.includes("sort=random")
              : pathname.startsWith(tab.href.split("?")[0]) && tab.href !== "/";

          return (
            <Link
              key={tab.label}
              href={tab.href}
              className={`flex flex-col items-center justify-center w-full h-full transition-colors ${
                isActive ? "text-txt" : "text-txt-muted"
              }`}
            >
              {isActive ? tab.iconFilled : tab.icon}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
